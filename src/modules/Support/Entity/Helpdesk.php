namespace Box\Mod\Support\Entity;

use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\DBAL\Types\Types;
use Doctrine\ORM\Mapping as ORM;
use FOSSBilling\Interfaces\ApiArrayInterface;
use FOSSBilling\Interfaces\TimestampInterface;
use FOSSBilling\Tools;

#[ORM\Entity(repositoryClass: \Box\Mod\Support\Repository\HelpdeskRepository::class)]
#[ORM\Table(name: 'support_helpdesk')]
#[ORM\HasLifecycleCallbacks]
class Helpdesk implements ApiArrayInterface, TimestampInterface
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column(type: Types::INTEGER)]
    /** @phpstan-ignore property.unusedType (Doctrine entity ID is auto-generated by the database) */
    private ?int $id = null;

    #[ORM\Column(type: Types::STRING, length: 255, nullable: true)]
    private ?string $name = null;

    #[ORM\Column(type: Types::STRING, length: 255, nullable: true)]
    private ?string $email = null;

    #[ORM\Column(name: 'can_reopen', type: Types::BOOLEAN, nullable: true)]
    private ?bool $canReopen = null;

    #[ORM\Column(name: 'close_after', type: Types::SMALLINT, nullable: true)]
    private ?int $closeAfter = null;

    #[ORM\Column(type: Types::TEXT, nullable: true)]
    private ?string $signature = null;

    #[ORM\Column(name: 'created_at', type: Types::DATETIME_MUTABLE, nullable: true)]
    private ?\DateTime $createdAt = null;

    #[ORM\Column(name: 'updated_at', type: Types::DATETIME_MUTABLE, nullable: true)]
    private ?\DateTime $updatedAt = null;

    /**
     * @var Collection<int, SupportTicket>
     */
    #[ORM\OneToMany(mappedBy: 'helpdesk', targetEntity: SupportTicket::class)]
    private Collection $tickets;

    public function __construct()
    {
        $this->tickets = new ArrayCollection();
    }

    public function toApiArray(\Model_Admin|\Model_Client|\Model_Guest|null $identity = null): array
    {
        if ($identity instanceof \Model_Admin) {
            return [
                'id' => $this->id,
                'name' => $this->name,
                'email' => $this->email,
                'can_reopen' => $this->canReopen,
                'close_after' => $this->closeAfter,
                'signature' => $this->signature,
                'created_at' => $this->createdAt?->format('Y-m-d H:i:s'),
                'updated_at' => $this->updatedAt?->format('Y-m-d H:i:s'),
            ];
        }

        return [
            'id' => $this->id,
            'name' => $this->name,
            'can_reopen' => (bool) $this->canReopen,
        ];
    }

    #[ORM\PrePersist]
    public function onPrePersist(): void
    {
        $now = new \DateTime();
        $this->createdAt ??= $now;
        $this->updatedAt = $now;
    }

    #[ORM\PreUpdate]
    public function onPreUpdate(): void
    {
        $this->updatedAt = new \DateTime();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(?string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getEmail(): ?string
    {
        return $this->email;
    }

    public function setEmail(?string $email): self
    {
        $this->email = $email;

        return $this;
    }

    public function canReopen(): bool
    {
        return (bool) $this->canReopen;
    }

    public function setCanReopen(mixed $canReopen): self
    {
        $this->canReopen = $canReopen === null ? null : Tools::normalizeBoolean($canReopen);

        return $this;
    }

    public function getCloseAfter(): ?int
    {
        return $this->closeAfter;
    }

    public function setCloseAfter(int|string|null $closeAfter): self
    {
        $this->closeAfter = $closeAfter === null || $closeAfter === '' ? null : (int) $closeAfter;

        return $this;
    }

    public function getSignature(): ?string
    {
        return $this->signature;
    }

    public function setSignature(?string $signature): self
    {
        $this->signature = $signature;

        return $this;
    }

    public function getCreatedAt(): ?\DateTime
    {
        return $this->createdAt;
    }

    public function setCreatedAt(\DateTime $createdAt): void
    {
        $this->createdAt = $createdAt;
    }

    public function getUpdatedAt(): ?\DateTime
    {
        return $this->updatedAt;
    }

    public function setUpdatedAt(\DateTime $updatedAt): void
    {
        $this->updatedAt = $updatedAt;
    }

    /**
     * @return Collection<int, SupportTicket>
     */
    public function getTickets(): Collection
    {
        return $this->tickets;
    }
}
